private void loadPatientRecords() {
    try {
        DatabaseAdapter db = new DatabaseAdapter();
        List<Map<String, String>> records = db.select("PatientInformation", null);
        
        DefaultTableModel model = new DefaultTableModel();
        

        model.addColumn("ID");
        model.addColumn("Name");
        model.addColumn("Phone");
        model.addColumn("Age");
        model.addColumn("Blood_Group");
        model.addColumn("Address");
        model.addColumn("Gender");
        model.addColumn("Any_Major_Disease");
        

        for (Map<String, String> record : records) {
            Vector<String> row = new Vector<>();
            row.add(record.get("Patient_ID"));
            row.add(record.get("Name"));
            row.add(record.get("Phone"));
            row.add(record.get("Age"));
            row.add(record.get("Blood_Group"));
            row.add(record.get("Address"));
            row.add(record.get("Gender"));
            row.add(record.get("Any_Major_Disease"));
            model.addRow(row);
        }
        
        AllPatientsTable.setModel(model);
        
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error loading records: " + ex.getMessage());
    }
}
