    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
    if (AllPatientsTable.isEditing()) {
        AllPatientsTable.getCellEditor().stopCellEditing();
    }
    int selectedRow = AllPatientsTable.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a patient to update");
        return;
    }
    
    try {
        DefaultTableModel model = (DefaultTableModel) AllPatientsTable.getModel();
        
        String patientId = model.getValueAt(selectedRow, 0).toString();
        String name = model.getValueAt(selectedRow, 1).toString();
        String phone = model.getValueAt(selectedRow, 2).toString();
        String age = model.getValueAt(selectedRow, 3).toString();
        String bloodGroup = model.getValueAt(selectedRow, 4).toString();
        String address = model.getValueAt(selectedRow, 5).toString();
        String gender = model.getValueAt(selectedRow, 6).toString();
        String majorDisease = model.getValueAt(selectedRow, 7).toString();
        
        Map<String, String> updateData = new HashMap<>();
        updateData.put("Name", name);
        updateData.put("Phone", phone);
        updateData.put("Age", age);
        updateData.put("Blood_Group", bloodGroup);
        updateData.put("Address", address);
        updateData.put("Gender", gender);
        updateData.put("Any_Major_Disease", majorDisease);

        boolean updated = dataAccess.updateData("PatientInformation", 
                                                updateData, 
                                                "Patient_ID = " + patientId);
        
        if (updated) {
            JOptionPane.showMessageDialog(this, 
                " Patient information updated successfully!", 
                "Success", 
                JOptionPane.INFORMATION_MESSAGE);
            loadPatientRecords(); 
        } else {
            JOptionPane.showMessageDialog(this, 
                "Update failed!", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
        
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, 
            "Error: " + ex.getMessage(), 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }

    }//GEN-LAST:event_UpdateButtonActionPerformed

    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed

        int selectedRow = AllPatientsTable.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a patient to delete");
        return;
    }
    
    try {
        String patientId = AllPatientsTable.getValueAt(selectedRow, 0).toString();
        String patientName = AllPatientsTable.getValueAt(selectedRow, 1).toString();
        
        int confirm = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to delete this patient?\n" +
            "Patient: " + patientName + "\n" +
            "ID: " + patientId,
            "Confirm Deletion",
            JOptionPane.YES_NO_OPTION
        );
        
        if (confirm == JOptionPane.YES_OPTION) {
            boolean deleted = dataAccess.deleteData("PatientInformation", "Patient_ID = " + patientId);
            
            if (deleted) {
                JOptionPane.showMessageDialog(this, "Patient deleted successfully!");
                loadPatientRecords();
            } else {
                JOptionPane.showMessageDialog(this, "Delete failed!");
            }
        }
        
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    }
    }//GEN-LAST:event_DeleteButtonActionPerformed

