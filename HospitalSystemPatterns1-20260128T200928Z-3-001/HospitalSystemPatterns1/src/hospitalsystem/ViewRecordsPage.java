    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // TODO add your handling code here:
        if (ViewAllMedicalRecordsTable.isEditing()) {
    ViewAllMedicalRecordsTable.getCellEditor().stopCellEditing();
}
    int selectedRow = ViewAllMedicalRecordsTable.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a record to update");
        return;
    }
    
    try {
        DefaultTableModel model = (DefaultTableModel) ViewAllMedicalRecordsTable.getModel();
        
        String recordId = model.getValueAt(selectedRow, 0).toString();
        String patientName = model.getValueAt(selectedRow, 1).toString();
        String patientAge = model.getValueAt(selectedRow, 2).toString();
        String doctorName = model.getValueAt(selectedRow, 3).toString();
        String date = model.getValueAt(selectedRow, 4).toString();
        String diagnosis = model.getValueAt(selectedRow, 5).toString();
        String prescription = model.getValueAt(selectedRow, 6).toString();
        String notes = model.getValueAt(selectedRow, 7).toString();
        
        Map<String, String> updateData = new HashMap<>();
        updateData.put("Patient_Name", patientName);
        updateData.put("Patient_Age", patientAge);
        updateData.put("Doctor_Name", doctorName);
        updateData.put("Date", date);
        updateData.put("Diagnosis", diagnosis);
        updateData.put("Prescription", prescription);
        updateData.put("Notes", notes);
        
        boolean updated = dataAccess.updateData("MedicalRecords", 
                                                updateData, 
                                                "Record_ID = " + recordId);
        
        if (updated) {
            JOptionPane.showMessageDialog(this, 
                "Medical record updated successfully!", 
                "Success", 
                JOptionPane.INFORMATION_MESSAGE);
            loadMedicalRecords(); 
        } else {
            JOptionPane.showMessageDialog(this, 
                "Update failed!", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
        
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, 
            "Error: " + ex.getMessage(), 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }


    }//GEN-LAST:event_UpdateButtonActionPerformed

    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = ViewAllMedicalRecordsTable.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a record to delete");
        return;
    }
    
    try {
        String recordId = ViewAllMedicalRecordsTable.getValueAt(selectedRow, 0).toString();
        String patientName = ViewAllMedicalRecordsTable.getValueAt(selectedRow, 1).toString();
        
        int confirm = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to delete this medical record?\n" +
            "Patient: " + patientName + "\n" +
            "Record ID: " + recordId + "\n\n" +
            "WARNING: This action cannot be undone!",
            "Confirm Deletion",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE
        );
        
        if (confirm == JOptionPane.YES_OPTION) {
               boolean deleted = dataAccess.deleteData("MedicalRecords", "Record_ID = " + recordId);
                
                if (deleted) {
                    JOptionPane.showMessageDialog(this, "Record deleted!");
                    loadMedicalRecords();
                } else {
                JOptionPane.showMessageDialog(this, "Delete failed!");
            }
        }
        
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    }
    }//GEN-LAST:event_DeleteButtonActionPerformed

