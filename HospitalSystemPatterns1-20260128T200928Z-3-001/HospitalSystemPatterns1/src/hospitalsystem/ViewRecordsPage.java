private void loadMedicalRecords() {
    try {
        DatabaseAdapter db = new DatabaseAdapter();
        List<Map<String, String>> records = db.select("MedicalRecords", null);
        
        DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("Record_ID");
        model.addColumn("Patient");
        model.addColumn("Age");
        model.addColumn("Doctor");
        model.addColumn("Date");
        model.addColumn("Diagnosis");
        model.addColumn("Prescription");
        model.addColumn("Notes");
        

        for (Map<String, String> record : records) {
            Vector<String> row = new Vector<>();
            row.add(record.get("Record_ID"));
            row.add(record.get("Patient_Name"));
            row.add(record.get("Patient_Age"));
            row.add(record.get("Doctor_Name"));
            row.add(record.get("Date"));
            row.add(record.get("Diagnosis"));
            row.add(record.get("Prescription"));
            row.add(record.get("Notes"));
            model.addRow(row);
        }
        
        ViewAllMedicalRecordsTable.setModel(model);
        
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error loading records: " + ex.getMessage());
    }
}    

