private void loadAppointments() {
    try {
        DatabaseAdapter db = new DatabaseAdapter();
        List<Map<String, String>> appointments = db.select("Appointments", null);
        
        DefaultTableModel model = new DefaultTableModel();
        

        model.addColumn("Appointments_ID");
        model.addColumn("Patient");
        model.addColumn("Age");
        model.addColumn("Doctor");
        model.addColumn("Date");
        model.addColumn("Time");
        model.addColumn("Status");
        
       
        for (Map<String, String> appointment : appointments) {
            Vector<String> row = new Vector<>();
            row.add(appointment.get("Appointment_ID"));
            row.add(appointment.get("Patient_Name"));
            row.add(appointment.get("Patient_Age"));
            row.add(appointment.get("Doctor_Name"));
            row.add(appointment.get("Date"));
            row.add(appointment.get("Time"));
            row.add(appointment.get("Status"));
            model.addRow(row);
        }
        
        AllBookingsTable.setModel(model);
        
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error loading appointments: " + ex.getMessage());
    }
}
