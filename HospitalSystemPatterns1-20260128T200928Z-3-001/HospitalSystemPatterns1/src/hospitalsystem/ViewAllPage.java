    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // TODO add your handling code here:
        if (AllBookingsTable.isEditing()) {
    AllBookingsTable.getCellEditor().stopCellEditing();
}

    int selectedRow = AllBookingsTable.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select an appointment to update");
        return;
    }
    
    try {
        DefaultTableModel model = (DefaultTableModel) AllBookingsTable.getModel();
        
        String appointmentId = model.getValueAt(selectedRow, 0).toString();
        String patientName = model.getValueAt(selectedRow, 1).toString();
        String patientAge = model.getValueAt(selectedRow, 2).toString();
        String doctorName = model.getValueAt(selectedRow, 3).toString();
        String date = model.getValueAt(selectedRow, 4).toString();
        String time = model.getValueAt(selectedRow, 5).toString();
        String status = model.getValueAt(selectedRow, 6).toString();
        
        Map<String, String> updateData = new HashMap<>();
        updateData.put("Patient_Name", patientName);
        updateData.put("Patient_Age", patientAge);
        updateData.put("Doctor_Name", doctorName);
        updateData.put("Date", date);
        updateData.put("Time", time);
        updateData.put("Status", status);
        
        boolean updated = dataAccess.updateData("Appointments", 
                                                updateData, 
                                                "Appointment_ID = " + appointmentId);
        
        if (updated) {
            JOptionPane.showMessageDialog(this, 
                "Appointment updated successfully!", 
                "Success", 
                JOptionPane.INFORMATION_MESSAGE);
            loadAppointments(); 
        } else {
            JOptionPane.showMessageDialog(this, 
                "Update failed!", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
        
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, 
            "Error: " + ex.getMessage(), 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }


    }//GEN-LAST:event_UpdateButtonActionPerformed

    private void CancelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CancelButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = AllBookingsTable.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select an appointment to cancel");
        return;
    }
    
    try {
        String appointmentId = AllBookingsTable.getValueAt(selectedRow, 0).toString();
        String patientName = AllBookingsTable.getValueAt(selectedRow, 1).toString();
        
        int confirm = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to cancel this appointment?\n" +
            "Patient: " + patientName + "\n" +
            "Appointment ID: " + appointmentId,
            "Confirm Cancellation",
            JOptionPane.YES_NO_OPTION
        );
        
        if (confirm == JOptionPane.YES_OPTION) {

            Map<String, String> updateData = new HashMap<>();
            updateData.put("Status", "Cancelled");
            
                boolean updated = dataAccess.updateData("Appointments", 
                    updateData, "Appointment_ID = " + appointmentId);
                
                if (updated) {
                    JOptionPane.showMessageDialog(this, "Appointment cancelled!");
                    loadAppointments(); }
                    else 
                    {
                JOptionPane.showMessageDialog(this, "Cancellation failed!");
            }
        }
        
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    }
    }//GEN-LAST:event_CancelButtonActionPerformed

